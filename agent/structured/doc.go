// 包 structured 提供结构化输出的生成、解析与校验能力。
//
// 该包用于约束模型输出格式，降低自由文本导致的解析失败风险，
// 支持 JSON Schema 定义、结构化生成、结果校验与错误定位。
//
// # 核心能力
//
//   - Schema 建模：定义对象、数组、枚举及约束规则
//   - 结构化生成：引导模型按指定结构输出
//   - 结果校验：对输出进行字段级校验与错误报告
//   - 失败恢复：支持重试与容错解析策略
//
// # 典型场景
//
//   - 工具调用参数生成
//   - 业务表单抽取
//   - 固定协议响应构建
//   - 评测结果结构化落盘
