// 包 a2a 提供 Agent-to-Agent（A2A）协议的核心实现。
//
// 该包用于智能体之间的跨进程、跨服务协作，围绕“可发现、可描述、可通信”
// 三个目标提供统一模型与基础设施能力。
//
// # 核心能力
//
//   - Agent Card：统一描述 Agent 元数据、能力、工具与输入输出 Schema
//   - 标准消息：支持任务、结果、错误、状态、取消等消息类型
//   - 客户端与服务端：提供 A2A 协议下的请求发送与消息处理能力
//   - 错误语义：内置结构化错误，便于上层实现重试、降级和诊断
//
// # 典型流程
//
//   1. 通过 Agent Card 声明能力与工具
//   2. 客户端发起任务消息到目标 Agent
//   3. 服务端执行并返回结果/状态/错误消息
//   4. 必要时通过取消消息中断长任务
//
// # 与 discovery 协同
//
// a2a 可与 `agent/discovery` 结合：
//   - 先按能力发现目标 Agent
//   - 再通过 A2A 协议建立通信与任务协作
