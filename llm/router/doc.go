// 包 router 提供多模型场景下的路由能力。
//
// 该包负责在多个 LLM 提供商与模型之间进行策略化选择，
// 目标是在质量、成本、时延与可用性之间取得平衡。
//
// # 路由能力
//
//   - 加权路由：基于权重、成本、时延、成功率等指标进行选择
//   - 前缀路由：按模型前缀或命名规则快速命中指定 Provider
//   - 语义路由：按意图分类结果选择更合适的模型集合
//   - 回退机制：首选模型失败后按候选顺序降级重试
//
// # 典型输入维度
//
// 路由通常会结合以下信号：
//   - 业务偏好（指定模型、标签、租户策略）
//   - SLA 约束（成本上限、时延上限、最低成功率）
//   - 运行时健康状态（可用性、历史成功率、平均延迟）
//
// # 与 llm 包协同
//
// router 作为 `llm.Provider` 之上的决策层，
// 可以无缝接入缓存、重试、观测、工具调用等能力，构建完整生产链路。
